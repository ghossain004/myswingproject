    public void saveMeeting() {
        String _meetingDate = meetingDate.getText();
        String _meetingTopic = meetingTopic.getText();
        String _meetingSpeaker = meetingSpeaker.getText();
        String _meetingTime = meetingTime.getText();
        String _meetingLocation = meetingLocation.getText();
        String _meetingStatus = meetingStatus.getText();

        try {
            String sql = "insert into meeting (meeting_date, meeting_topic, meeting_speaker, meeting_time, meeting_location, join_status) value (?, ?, ?, ?, ?, ?)";

            Connection con = DatabaseConnection.getDBConnection();
            PreparedStatement pstm = con.prepareStatement(sql);
            pstm.setString(1, _meetingDate);
            pstm.setString(2, _meetingTopic);
            pstm.setString(3, _meetingSpeaker);
            pstm.setString(4, _meetingTime);
            pstm.setString(5, _meetingLocation);
            pstm.setString(6, _meetingStatus);
            int status = pstm.executeUpdate();
            JOptionPane.showMessageDialog(this, status);
        } catch (Exception e) {
            System.out.println(e);
        }
    }

