    private void saveleaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveleaveBtnActionPerformed
        // TODO add your handling code here:
        String _leaveEmpId = leaveEmpId.getText();
        String _leaveFirstName = leaveFirstName.getText();
        String _leaveLastName = leaveLastName.getText();
        String _fromDate = fromDate.getText();
        String _toDate = toDate.getText();
        String _leaveDuration = leaveDuration.getText();
        String _leaveReason = leaveReason.getText();
        
        try {
            String sql = "insert into leave (employee_id, first_name, last_name, from_date, to_date, duration, leave_reason) value (?, ?, ?, ?, ?, ?, ?)";
            
            Connection con = DatabaseConnection.getDBConnection();
            PreparedStatement pstm = con.prepareStatement(sql);
            pstm.setString(1, _leaveEmpId);
            pstm.setString(2, _leaveFirstName);
            pstm.setString(3, _leaveLastName);
            pstm.setString(4, _fromDate);
            pstm.setString(5, _toDate);
            pstm.setString(6, _leaveDuration);
            pstm.setString(7, _leaveReason);
            int status = pstm.executeUpdate();
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Submitted");
            }else{
                JOptionPane.showMessageDialog(this, "Retry");
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_saveleaveBtnActionPerformed

